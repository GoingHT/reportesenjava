    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    String usuarioValido = "usuario1";  // Este es el usuario válido
    String contraValida = "12345"; // Y esta la contraseña válida

    // Aquí se recogen los resultados proporcionados por el usuario
    String usuario = jTextField1.getText();
    String contra = new String(jPasswordField1.getPassword());

    // Se validan los datos.
    if (usuario.equals(usuarioValido) && contra.equals(contraValida)) {
        // Si el usuario y la contraseña son válidos, se abre el siguiente formulario
        moduloReportes reportes = new moduloReportes();
        reportes.setVisible(true);
        
        // Se cierra el formmulario de inicio de sesión
        this.dispose();
    } else {
        // Si son incorrectos, muestra el mensaje de error
        javax.swing.JOptionPane.showMessageDialog(this, "Usuario o contraseña incorrectos", "Error de inicio de sesión", javax.swing.JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_jButton1ActionPerformed

